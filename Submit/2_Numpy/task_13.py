# 課題13: 次の連立方程式を逆行列を用いて算出せよ
# 
# 回答はそれぞれ「x」, 「y」に格納すること
# 
# \begin{eqnarray*}
# \ 3x + 6y = 18
# \\
# \ 2x + 2y = 10
# \end{eqnarray*}
# 


# 以下よりコードを記入してください  ##############################
a = np.array([[3,6],[2,2]])
b = np.array([[18],[10]])

inv_a = np.linalg.inv(a)
c = np.dot(inv_a,b)

x = c[0][0]
y = c[1][0]

# 出力  #################################################
print('x =', x)
print('y =', y)
